
public abstract class Animal { //nao da para criar objeto animal
	private String nome;
	private int idade;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	public abstract void correr(); //obrigatorio criar o metodo em todas as classes
	
	
}
